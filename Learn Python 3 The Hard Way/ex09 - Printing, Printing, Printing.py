# These are variables with strings in them. Also a newline character has been
# the \n.
days = "Mon Tue Wed Thu Fri Sat Sun"
months = "\nJan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"

# The print function prints a string
# with the corresponding value of the variables above.
print("Here are the days: ", days)
print("Here are the months: ", months)

# This is another way to use the print function. With 3 quotes you can write as
# many lines until you end with another 3 quotes.
print("""
There's someting going on here.
With the three double-quotes.
We'll be able to type as much as we like.
Even 4 lines if we want, or 5, or 6.
""")
